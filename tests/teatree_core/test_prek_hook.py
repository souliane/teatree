"""Regression tests for prek pre-push hook stale-path coupling.

souliane/teatree#1462 — ``prek install`` bakes the install-time absolute path
of the binary into the SHARED ``.git/hooks/pre-push`` (``PREK="<abs>"``). The
hooks dir is shared across every worktree, so once the worktree that
provisioned it is torn down the baked path is stale and ``exec "$PREK"`` hangs
or fails for every sibling worktree. ``--no-verify`` became the workaround,
killing the pre-push gate.

Two complementary fixes are covered here.

Harden on install — after ``prek install`` the hook is rewritten so ``PREK``
resolves via PATH, never a baked absolute path. Version-independent: it holds
whether or not prek's own template carries a PATH fallback.

Reap on teardown — tearing down a worktree removes only the SHARED hooks baked
with a path inside that worktree, so the OTHER worktrees' pushes keep running
the hook to completion (the acceptance criterion).
"""

import re
import shutil
import subprocess
from pathlib import Path

import pytest

from teatree.core import prek_hook

_GIT_BIN = shutil.which("git") or "/usr/bin/git"

_STALE_PRE_PUSH = """#!/bin/sh
# File generated by prek: https://github.com/j178/prek
# ID: deadbeef

HERE="$(cd "$(dirname "$0")" && pwd)"
PREK="{prek_path}"

exec "$PREK" hook-impl --hook-dir "$HERE" --script-version 4 --hook-type=pre-push -- "$@"
"""


def _git(repo: Path, *args: str) -> None:
    subprocess.run([_GIT_BIN, "-C", str(repo), *args], check=True, capture_output=True)


@pytest.fixture
def main_clone(tmp_path: Path) -> Path:
    main = tmp_path / "main"
    main.mkdir()
    _git(main, "init", "--initial-branch=main", "-q")
    _git(main, "config", "user.email", "test@example.com")
    _git(main, "config", "user.name", "test")
    _git(main, "commit", "--allow-empty", "-m", "root")
    return main


def _hooks_dir(repo: Path) -> Path:
    proc = subprocess.run(
        [_GIT_BIN, "-C", str(repo), "rev-parse", "--git-path", "hooks"],
        check=True,
        capture_output=True,
        text=True,
    )
    hooks = Path(proc.stdout.strip())
    if not hooks.is_absolute():
        hooks = (repo / hooks).resolve()
    hooks.mkdir(parents=True, exist_ok=True)
    return hooks


def _write_stale_hook(repo: Path, name: str, prek_path: str) -> Path:
    hook = _hooks_dir(repo) / name
    hook.write_text(_STALE_PRE_PUSH.format(prek_path=prek_path))
    hook.chmod(0o755)
    return hook


class TestHardenHooks:
    def test_baked_absolute_path_rewritten_to_path_resolution(self, main_clone: Path) -> None:
        gone = main_clone.parent / "s-teatree-941-gone" / ".venv" / "bin" / "prek"
        hook = _write_stale_hook(main_clone, "pre-push", str(gone))

        prek_hook.harden_hooks(str(main_clone))

        body = hook.read_text()
        assert str(gone) not in body, f"stale absolute path survived hardening:\n{body}"
        assert 'PREK="prek"' in body, f"hook does not resolve prek via PATH:\n{body}"
        assert "hook-impl" in body, "hardening must not destroy the dispatch line"

    def test_idempotent_on_already_path_resolved_hook(self, main_clone: Path) -> None:
        hook = _write_stale_hook(main_clone, "pre-push", "prek")
        before = hook.read_text()

        prek_hook.harden_hooks(str(main_clone))

        assert hook.read_text() == before

    def test_leaves_non_prek_hooks_untouched(self, main_clone: Path) -> None:
        hook = _hooks_dir(main_clone) / "pre-push"
        custom = '#!/bin/sh\nPREK="/some/other/tool"\nexec "$PREK" "$@"\n'
        hook.write_text(custom)

        prek_hook.harden_hooks(str(main_clone))

        assert hook.read_text() == custom


class TestRemoveStaleHooks:
    def test_removes_shared_hook_baked_into_torn_down_worktree(self, main_clone: Path) -> None:
        torn = main_clone.parent / "torn-down-wt"
        hook = _write_stale_hook(main_clone, "pre-push", str(torn / ".venv" / "bin" / "prek"))

        cleaned = prek_hook.remove_stale_hooks(str(main_clone), str(torn))

        assert not hook.exists(), "stale hook baked into the torn-down worktree must be removed"
        assert str(hook) in cleaned

    def test_keeps_hook_baked_into_a_different_worktree(self, main_clone: Path) -> None:
        other = main_clone.parent / "still-alive-wt"
        hook = _write_stale_hook(main_clone, "pre-push", str(other / ".venv" / "bin" / "prek"))

        cleaned = prek_hook.remove_stale_hooks(str(main_clone), str(main_clone.parent / "torn-down-wt"))

        assert hook.exists(), "must not remove a hook coupled to a live sibling worktree"
        assert cleaned == []

    def test_keeps_path_resolved_hook(self, main_clone: Path) -> None:
        hook = _write_stale_hook(main_clone, "pre-push", "prek")

        cleaned = prek_hook.remove_stale_hooks(str(main_clone), str(main_clone.parent / "torn-down-wt"))

        assert hook.exists(), "a PATH-resolved hook is never stale and must survive any teardown"
        assert cleaned == []


@pytest.mark.skipif(shutil.which("prek") is None, reason="prek not on PATH")
class TestInstallProducesPathResolvedHook:
    def test_install_hook_never_carries_a_stale_absolute_only_path(self, main_clone: Path) -> None:
        wt = main_clone.parent / "wt"
        _git(main_clone, "worktree", "add", "-q", str(wt), "-b", "feature/1462")
        (wt / ".pre-commit-config.yaml").write_text(
            "default_install_hook_types: [pre-push]\n"
            "default_stages: [pre-push]\n"
            "repos:\n"
            "    - repo: local\n"
            "      hooks:\n"
            "          - id: noop\n"
            "            name: noop\n"
            "            language: system\n"
            "            entry: 'true'\n"
            "            pass_filenames: false\n"
            "            always_run: true\n"
        )

        result = prek_hook.install(str(wt))
        assert result.success, f"prek install failed: {result.error}"

        hook = _hooks_dir(main_clone) / "pre-push"
        assert hook.is_file()
        # The PRIMARY assignment must resolve via PATH. prek's own template
        # bakes ``PREK="<abs>"`` and (in recent versions) adds a fallback that
        # also writes ``PREK="prek"`` — so a substring check passes even on the
        # buggy hook. Pin the primary assignment line itself.
        primary = next(
            (ln.strip() for ln in hook.read_text().splitlines() if re.fullmatch(r'PREK="[^"]*"', ln.strip())),
            "",
        )
        assert primary == 'PREK="prek"', (
            "installed hook's primary PREK assignment still bakes an absolute path — "
            f"stale-path hang class (souliane/teatree#1462):\n{hook.read_text()}"
        )
